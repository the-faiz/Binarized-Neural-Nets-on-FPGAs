// =========================================================================
// MAIN EXECUTION
// =========================================================================
int main()
{
    print("\n\n=======================================\n");
    print("--- Zynq Hardware CNN Accelerator ---\n");
    print("=======================================\n");

    // 1. Hardware Reset
    // We mapped ~slv_reg2[0] to the CNN's rst_n pin.
    // So writing 1 asserts reset, writing 0 releases it.
    print("[1/3] Resetting CNN Hardware...\n");
    Xil_Out32(CNN_BASE_ADDR + REG_RESET, 0x01); 
    usleep(100); // Give the hardware 100 microseconds to clear its registers
    Xil_Out32(CNN_BASE_ADDR + REG_RESET, 0x00); 

    // 2. Stream the Image via AXI
    print("[2/3] Streaming 1024 pixels to FPGA...\n");
    for(int i = 0; i < 1024; i++) {
        // Writing to this address triggers the 1-cycle `pixel_valid_in` pulse in Verilog
        Xil_Out32(CNN_BASE_ADDR + REG_PIXEL_IN, image_data[i]);
    }

    // 3. Poll for the Result
    print("[3/3] Waiting for Neural Network computation...\n");
    u32 status = 0;
    
    // In our Verilog, we mapped Register 1 (0x04) to: {27'd0, cnn_valid, cnn_result}
    // Bit 4 (0x10) is the 'cnn_valid' flag. We wait in this loop until it becomes 1.
    while((status & 0x00000010) == 0) { 
        status = Xil_In32(CNN_BASE_ADDR + REG_RESULT);
    }

    // Bits 3:0 (0x0F) hold the actual 4-bit classification prediction
    u32 prediction = status & 0x0000000F; 
    
    // 4. Print the Verdict!
    printf("\n");
    printf("****************************************\n");
    printf("   FINAL HARDWARE PREDICTION : %lu \n", prediction);
    printf("****************************************\n\n");

    return 0;
}